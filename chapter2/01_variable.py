# Integer variable
age = 21
print(age)  # Output: 21

# String variable
name = "Anjali"
print(name)  # Output: Anjali

# Float variable
height = 5.4
print(height)  # Output: 5.4

# Boolean variable
is_student = True
print(is_student)  # Output: True

a = b = c = "Python"
print(a, b, c)  # Output: Python Python Python

x, y, z = 10, 20, 30
print(x, y, z)  # Output: 10 20 30
