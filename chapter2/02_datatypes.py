# 1. Integer (int) – Whole numbers
a = 10
print(a)            
print(type(a))      

# 2. Float – Decimal numbers
b = 5.5
print(b)            
print(type(b))     

# 3. String (str) – Text
name = "Anjali"
print(name)         
print(type(name))   

# 4. Boolean (bool) – True or False
is_girl = True
print(is_girl)      
print(type(is_girl))

# 5. List – Group of items (can change)
fruits = ["apple", "banana", "cherry"]
print(fruits)       
print(type(fruits))

# 6. Tuple – Group of items (cannot change)
colors = ("red", "green", "blue")
print(colors)       
print(type(colors)) 

# 7. Set – Unordered, no duplicate items
nums = {1, 2, 3, 2}
print(nums)         
print(type(nums))  

# 8. Dictionary – Key-value pairs
student = {
    "name": "Anjali",
    "age": 21
}
print(student)      
print(type(student))

# 9. None – Represents 'nothing'
empty = None
print(empty)        
print(type(empty))  
